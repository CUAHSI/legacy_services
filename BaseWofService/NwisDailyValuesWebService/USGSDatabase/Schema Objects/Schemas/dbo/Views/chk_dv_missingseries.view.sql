                      series.alt_va, series.alt_acy_va, series.alt_datum_cd, series.data_type_cd, series.parm_cd, series.stat_cd, series.dd_nu, series.loc_web_ds, 
                      series.medium_grp_cd, series.parm_grp_cd, series.srs_id, series.access_cd, series.begin_date, series.end_date, series.count_nu, series.beginDate, 
                      series.endDate
FROM         dbo.series AS series LEFT OUTER JOIN
                      dbo.odm_dv_series AS odmSeries ON odmSeries.siteID = series.site_no AND odmSeries.usgs_parm_cd = series.parm_cd AND 
                      odmSeries.usgs_stat_cd = series.stat_cd
WHERE     (series.data_type_cd = 'dv') AND (odmSeries.siteID IS NULL)

